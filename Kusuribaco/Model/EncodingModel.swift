    // URLエンコードをする(eucjp)
    func eucjpPercentEncoding(letter: String) -> String {
        
        // エンコード後の文字列
        var percentLetter: String = ""
        // エンコード対象外とする文字列
        let pattern: String = "[0-9a-zA-Z_.*-]"
        let regex = try? NSRegularExpression(pattern: pattern, options: NSRegularExpression.Options())
        
        // エンコードする
        if regex!.firstMatch(in: letter, options: NSRegularExpression.MatchingOptions(), range: NSMakeRange(0, letter.count)) != nil {
            // エンコード対象外の場合
            percentLetter = letter
        
        }else{
            // エンコードする場合
            if encodeDictionary.keys.contains(letter) {
                // 該当する文字列が存在する場合は、URLエンコーディングして返す
                percentLetter = encodeDictionary[letter]!
            }else{
                // 該当する文字列が存在しない場合は、半角スペースとして返す
                percentLetter = "+"
            }
        }

        return percentLetter
    }
    
    // URLエンコードをする(utf8)
    func utf8PercentEncoding(URLString: String) -> String {
        
        let allowedCharacters = NSCharacterSet.alphanumerics.union(.init(charactersIn: "-._~"))
        return URLString.addingPercentEncoding(withAllowedCharacters: allowedCharacters)!
    }
